package java_middel_topic;


public class PlayerInformation {
    
    private String game_name;
    private String game_career;
    private int game_level;
    private int game_atk;
    private int game_def;
    private int game_hp;
    private int game_str;
    private int game_int;
    private int game_exp;
    /**
     * @return the game_name
     */
    public String getGame_name() {
        return game_name;
    }

    /**
     * @param game_name the game_name to set
     */
    public void setGame_name(String game_name) {
        this.game_name = game_name;
    }

    /**
     * @return the game_career
     */
    public String getGame_career() {
        return game_career;
    }

    /**
     * @param game_career the game_career to set
     */
    public void setGame_career(String game_career) {
        this.game_career = game_career;
    }

    /**
     * @return the game_level
     */
    public int getGame_level() {
        return game_level;
    }

    /**
     * @param game_level the game_level to set
     */
    public void setGame_level(int game_level) {
        this.game_level = game_level;
    }

    /**
     * @return the game_atk
     */
    public int getGame_atk() {
        return game_atk;
    }

    /**
     * @param game_atk the game_atk to set
     */
    public void setGame_atk(int game_atk) {
        this.game_atk = game_atk;
    }

    /**
     * @return the game_def
     */
    public int getGame_def() {
        return game_def;
    }

    /**
     * @param game_def the game_def to set
     */
    public void setGame_def(int game_def) {
        this.game_def = game_def;
    }

    /**
     * @return the game_hp
     */
    public int getGame_hp() {
        return game_hp;
    }

    /**
     * @param game_hp the game_hp to set
     */
    public void setGame_hp(int game_hp) {
        this.game_hp = game_hp;
    }

    /**
     * @return the game_str
     */
    public int getGame_str() {
        return game_str;
    }

    /**
     * @param game_str the game_str to set
     */
    public void setGame_str(int game_str) {
        this.game_str = game_str;
    }

    /**
     * @return the game_int
     */
    public int getGame_int() {
        return game_int;
    }

    /**
     * @param game_int the game_int to set
     */
    public void setGame_int(int game_int) {
        this.game_int = game_int;
    }

    /**
     * @return the game_exp
     */
    public int getGame_exp() {
        return game_exp;
    }

    /**
     * @param game_exp the game_exp to set
     */
    public void setGame_exp(int game_exp) {
        this.game_exp = game_exp;
    }
    
}
